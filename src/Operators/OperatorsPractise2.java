package Operators;

public class OperatorsPractise2 {
    public static void main(String[] args) {
        double a=5;

        a++; // 6
        System.out.println(a);

        a--; // 5
        System.out.println(a);

        a+=4; // 4-4 artir (qısa yazılış)
        System.out.println(a);

        a=a+4; // (uzun yazılış)

        a-=2; // 2-2 azalt (qısa yazılış)
        System.out.println(a);

        a=a-2; // (uzun yazılış)

        a*=2;// a=a*2

        a/=2; // a=a/2

    }
}
